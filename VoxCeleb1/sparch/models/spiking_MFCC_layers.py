class BSA_Layer(nn.Module):
    def __init__(self, num_feature) -> None:
        super().__init__()
        self.num_feature = num_feature
    def forward(self, x):
        bsa = encoder.BSA(x.cpu().numpy())
        spikes = bsa.get_spikes()
        return torch.tensor(spikes).to(x.device)
    
class TBR_Layer(nn.Module):
    def __init__(self, num_feature) -> None:
        super().__init__()
        self.num_feature = num_feature
    def forward(self, x):
        bsa = encoder.TBR(torch.sigmoid(x).cpu().numpy())
        spikes = bsa.get_spikes()
        return torch.tensor(spikes).to(x.device)